fn generate_helpers() -> String {
    r#"// Code generated by kreuzcrawl-e2e-generator. DO NOT EDIT.

use wiremock::matchers::{method, path};
use wiremock::{Mock, MockServer, ResponseTemplate};

/// Start a new mock HTTP server.
pub async fn setup_mock_server() -> MockServer {
    MockServer::start().await
}

/// Load a response body file from fixtures/responses/.
pub fn load_response_body(relative_path: &str) -> String {
    let base = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("e2e/rust parent")
        .parent()
        .expect("e2e parent")
        .join("fixtures")
        .join("responses");
    let full_path = base.join(relative_path);
    std::fs::read_to_string(&full_path)
        .unwrap_or_else(|e| panic!("failed to load response body {}: {e}", full_path.display()))
}

/// Register a mock route on the given server.
pub async fn register_mock(
    server: &MockServer,
    http_method: &str,
    route: &str,
    status: u16,
    headers: &[(&str, &str)],
    body: &str,
) {
    let mut response = ResponseTemplate::new(status).set_body_string(body.to_owned());
    for &(key, value) in headers {
        response = response.append_header(key, value);
    }
    Mock::given(method(http_method))
        .and(path(route))
        .respond_with(response)
        .mount(server)
        .await;
}
"#
    .to_owned()
}

fn generate_category_file(_category: &str, fixtures: &[&Fixture]) -> Result<String> {
    let mut out = String::with_capacity(4096);

    writeln!(
        out,
        "// Code generated by kreuzcrawl-e2e-generator. DO NOT EDIT."
    )?;
    writeln!(out, "#![allow(clippy::too_many_lines)]")?;
    writeln!(out)?;
    writeln!(out, "use e2e_rust::helpers;")?;

    // Add FeedType import if any fixture uses feed assertions
    let needs_feed_type = fixtures.iter().any(|f| {
        f.assertions
            .as_ref()
            .and_then(|a| a.feeds.as_ref())
            .is_some()
    });
    if needs_feed_type {
        writeln!(out, "use kreuzcrawl::FeedType;")?;
    }

    writeln!(out)?;

    for fixture in fixtures {
        generate_test_fn(&mut out, fixture)?;
        writeln!(out)?;
    }

    Ok(out)
}

fn generate_test_fn(out: &mut String, fixture: &Fixture) -> Result<()> {
    let fn_name = sanitize_identifier(&fixture.id);
    let category = fixture.category();

    writeln!(out, "#[tokio::test]")?;
    writeln!(out, "async fn test_{fn_name}() {{")?;
    writeln!(out, "    // {}", escape_rust_string(&fixture.description))?;

    // Setup mock server
    writeln!(out, "    let mock = helpers::setup_mock_server().await;")?;

    // Handle multi-route fixtures (crawl scenarios with mock_responses array)
    if let Some(ref routes) = fixture.mock_responses {
        for (i, route) in routes.iter().enumerate() {
            let body_var = format!("body_{i}");
            if let Some(ref body_file) = route.body_file {
                writeln!(
                    out,
                    "    let {body_var} = helpers::load_response_body(\"{}\");",
                    escape_rust_string(body_file)
                )?;
            } else if let Some(ref body_inline) = route.body_inline {
                writeln!(
                    out,
                    "    let {body_var} = \"{}\".to_owned();",
                    escape_rust_string(body_inline)
                )?;
            } else {
                writeln!(out, "    let {body_var} = String::new();")?;
            }
            let headers_str = route
                .headers
                .iter()
                .map(|(k, v)| {
                    format!(
                        "(\"{}\", \"{}\")",
                        escape_rust_string(k),
                        escape_rust_string(v)
                    )
                })
                .collect::<Vec<_>>()
                .join(", ");
            writeln!(
                out,
                "    helpers::register_mock(&mock, \"{}\", \"{}\", {}, &[{headers_str}], &{body_var}).await;",
                escape_rust_string(&route.method),
                escape_rust_string(&route.path),
                route.status_code,
            )?;
        }
    } else if let Some(ref mock_resp) = fixture.mock_response {
        // Single-route fixture
        if let Some(ref body_file) = mock_resp.body_file {
            writeln!(
                out,
                "    let body = helpers::load_response_body(\"{}\");",
                escape_rust_string(body_file)
            )?;
        } else if let Some(ref body_inline) = mock_resp.body_inline {
            writeln!(
                out,
                "    let body = \"{}\".to_owned();",
                escape_rust_string(body_inline)
            )?;
        } else {
            writeln!(out, "    let body = String::new();")?;
        }

        let headers_str = mock_resp
            .headers
            .iter()
            .map(|(k, v)| {
                format!(
                    "(\"{}\", \"{}\")",
                    escape_rust_string(k),
                    escape_rust_string(v)
                )
            })
            .collect::<Vec<_>>()
            .join(", ");

        let route = fixture
            .request
            .as_ref()
            .map(|r| extract_route(&r.url))
            .unwrap_or_else(|| "/".to_owned());
        let method = fixture
            .request
            .as_ref()
            .map(|r| r.method.clone())
            .unwrap_or_else(|| "GET".to_owned());

        writeln!(
            out,
            "    helpers::register_mock(&mock, \"{}\", \"{}\", {}, &[{headers_str}], &body).await;",
            escape_rust_string(&method),
            escape_rust_string(&route),
            mock_resp.status_code,
        )?;

        // For robots category, also register at /robots.txt
        if category == "robots" && route != "/robots.txt" {
            writeln!(
                out,
                "    helpers::register_mock(&mock, \"GET\", \"/robots.txt\", {}, &[{headers_str}], &body).await;",
                mock_resp.status_code,
            )?;
        }
    }
    writeln!(out)?;

    // Generate config
    generate_config(out, fixture)?;
    writeln!(out)?;

    // Generate the appropriate API call based on category
    match category {
        "crawl" | "cookies" | "redirect" => {
            writeln!(
                out,
                "    let result = kreuzcrawl::crawl(&mock.uri(), &config).await;"
            )?;
        }
        "sitemap" | "map" => {
            writeln!(
                out,
                "    let result = kreuzcrawl::map(&mock.uri(), &config).await;"
            )?;
        }
        // Single-page categories: scrape, metadata, links, robots, content, auth, error
        _ => {
            writeln!(
                out,
                "    let result = kreuzcrawl::scrape(&mock.uri(), &config).await;"
            )?;
        }
    }

    // For error category, handle Result differently
    if category == "error" {
        generate_error_assertions(out, fixture)?;
    } else {
        writeln!(
            out,
            "    let result = result.expect(\"request should succeed\");"
        )?;
        if let Some(ref assertions) = fixture.assertions {
            generate_assertions(out, assertions, category)?;
        }
    }

    writeln!(out, "}}")?;
    Ok(())
}

fn generate_config(out: &mut String, fixture: &Fixture) -> Result<()> {
    writeln!(out, "    let config = kreuzcrawl::CrawlConfig {{")?;

    if let Some(ref cfg) = fixture.config {
        if let Some(max_depth) = cfg.max_depth {
            writeln!(out, "        max_depth: Some({max_depth}),")?;
        }
        if let Some(max_pages) = cfg.max_pages {
            writeln!(out, "        max_pages: Some({max_pages}),")?;
        }
        if let Some(respect_robots) = cfg.respect_robots_txt {
            writeln!(out, "        respect_robots_txt: {respect_robots},")?;
        }
        if let Some(timeout_ms) = cfg.request_timeout_ms {
            writeln!(
                out,
                "        request_timeout: std::time::Duration::from_millis({timeout_ms}),"
            )?;
        }
        if let Some(ref ua) = cfg.user_agent {
            writeln!(
                out,
                "        user_agent: Some(\"{}\".to_owned()),",
                escape_rust_string(ua)
            )?;
        }
        if let Some(stay) = cfg.stay_on_domain {
            writeln!(out, "        stay_on_domain: {stay},")?;
        }
        if let Some(ref paths) = cfg.include_paths {
            let items: Vec<String> = paths
                .iter()
                .map(|p| format!("\"{}\".to_owned()", escape_rust_string(p)))
                .collect();
            writeln!(
                out,
                "        include_paths: Some(vec![{}]),",
                items.join(", ")
            )?;
        }
        if let Some(ref paths) = cfg.exclude_paths {
            let items: Vec<String> = paths
                .iter()
                .map(|p| format!("\"{}\".to_owned()", escape_rust_string(p)))
                .collect();
            writeln!(
                out,
                "        exclude_paths: Some(vec![{}]),",
                items.join(", ")
            )?;
        }
        if let Some(allow_sub) = cfg.allow_subdomains {
            writeln!(out, "        allow_subdomains: {allow_sub},")?;
        }
        if let Some(max_redir) = cfg.max_redirects {
            writeln!(out, "        max_redirects: Some({max_redir}),")?;
        }
        if let Some(max_body) = cfg.max_body_size {
            writeln!(out, "        max_body_size: Some({max_body}),")?;
        }
        if let Some(max_conc) = cfg.max_concurrent {
            writeln!(out, "        max_concurrent: Some({max_conc}),")?;
        }
        if let Some(ref headers) = cfg.custom_headers {
            let items: Vec<String> = headers
                .iter()
                .map(|(k, v)| {
                    format!(
                        "(\"{}\".to_owned(), \"{}\".to_owned())",
                        escape_rust_string(k),
                        escape_rust_string(v)
                    )
                })
                .collect();
            writeln!(
                out,
                "        custom_headers: Some(vec![{}].into_iter().collect()),",
                items.join(", ")
            )?;
        }
        if let Some(cookies) = cfg.cookies_enabled {
            writeln!(out, "        cookies_enabled: {cookies},")?;
        }
        if let Some(ref auth) = cfg.auth_basic {
            writeln!(
                out,
                "        auth_basic: Some((\"{}\".to_owned(), \"{}\".to_owned())),",
                escape_rust_string(&auth.username),
                escape_rust_string(&auth.password)
            )?;
        }
        if let Some(ref bearer) = cfg.auth_bearer {
            writeln!(
                out,
                "        auth_bearer: Some(\"{}\".to_owned()),",
                escape_rust_string(bearer)
            )?;
        }
        if let Some(ref auth_hdr) = cfg.auth_header {
            writeln!(
                out,
                "        auth_header: Some((\"{}\".to_owned(), \"{}\".to_owned())),",
                escape_rust_string(&auth_hdr.name),
                escape_rust_string(&auth_hdr.value)
            )?;
        }
        if let Some(retry) = cfg.retry_count {
            writeln!(out, "        retry_count: Some({retry}),")?;
        }
        if let Some(ref codes) = cfg.retry_codes {
            let items: Vec<String> = codes.iter().map(|c| c.to_string()).collect();
            writeln!(
                out,
                "        retry_codes: Some(vec![{}]),",
                items.join(", ")
            )?;
        }
    }

    writeln!(out, "        ..Default::default()")?;
    writeln!(out, "    }};")?;
    Ok(())
}

fn generate_assertions(out: &mut String, assertions: &Assertions, category: &str) -> Result<()> {
    if let Some(code) = assertions.status_code {
        writeln!(out, "    assert_eq!(result.status_code, {code});")?;
    }
    if let Some(ref ct) = assertions.content_type {
        writeln!(
            out,
            "    assert!(result.content_type.starts_with(\"{}\"));",
            escape_rust_string(ct)
        )?;
    }
    if let Some(not_empty) = assertions.html_not_empty {
        if not_empty {
            writeln!(out, "    assert!(!result.html.is_empty());")?;
        } else {
            writeln!(out, "    assert!(result.html.is_empty());")?;
        }
    }

    if let Some(ref meta) = assertions.metadata {
        generate_metadata_assertions(out, meta)?;
    }
    if let Some(ref links) = assertions.links {
        generate_link_assertions(out, links)?;
    }
    if let Some(ref images) = assertions.images {
        generate_image_assertions(out, images)?;
    }
    if let Some(ref og) = assertions.og {
        generate_og_assertions(out, og)?;
    }
    if let Some(ref twitter) = assertions.twitter {
        generate_twitter_assertions(out, twitter)?;
    }
    if let Some(ref dc) = assertions.dublin_core {
        generate_dublin_core_assertions(out, dc)?;
    }
    if let Some(ref jl) = assertions.json_ld {
        generate_json_ld_assertions(out, jl)?;
    }
    if let Some(ref feeds) = assertions.feeds {
        generate_feed_assertions(out, feeds)?;
    }
    if let Some(ref robots) = assertions.robots {
        generate_robots_assertions(out, robots, category)?;
    }
    if let Some(ref sitemap) = assertions.sitemap {
        generate_sitemap_assertions(out, sitemap)?;
    }
    if let Some(ref crawl) = assertions.crawl {
        generate_crawl_assertions(out, crawl)?;
    }
    if let Some(ref redirect) = assertions.redirect {
        generate_redirect_assertions(out, redirect)?;
    }
    if let Some(ref content) = assertions.content {
        generate_content_assertions(out, content)?;
    }
    if let Some(ref cookies) = assertions.cookies {
        generate_cookie_assertions(out, cookies)?;
    }
    if let Some(ref auth) = assertions.auth {
        generate_auth_assertions(out, auth)?;
    }
    if let Some(ref map) = assertions.map {
        generate_map_assertions(out, map)?;
    }

    Ok(())
}

fn generate_error_assertions(out: &mut String, fixture: &Fixture) -> Result<()> {
    let assertions = fixture.assertions.as_ref();
    let error = assertions.and_then(|a| a.error.as_ref());

    if let Some(err) = error {
        if err.is_error == Some(true) {
            writeln!(
                out,
                "    let err = result.expect_err(\"request should fail\");"
            )?;
            if let Some(ref error_type) = err.error_type {
                writeln!(
                    out,
                    "    assert!(err.to_string().contains(\"{}\"));",
                    escape_rust_string(error_type)
                )?;
            }
        } else {
            writeln!(
                out,
                "    let result = result.expect(\"request should succeed\");"
            )?;
            if let Some(ref other_assertions) = fixture.assertions {
                generate_assertions(out, other_assertions, "error")?;
            }
        }
    } else {
        writeln!(out, "    let _result = result;")?;
    }

    Ok(())
}

fn generate_metadata_assertions(out: &mut String, meta: &MetadataAssertions) -> Result<()> {
    if let Some(ref title) = meta.title {
        writeln!(
            out,
            "    assert_eq!(result.metadata.title.as_deref(), Some(\"{}\"));",
            escape_rust_string(title)
        )?;
    }
    if let Some(ref desc) = meta.description_contains {
        writeln!(
            out,
            "    assert!(result.metadata.description.as_ref().map(|d| d.contains(\"{}\")).unwrap_or(false));",
            escape_rust_string(desc)
        )?;
    }
    if let Some(has_canonical) = meta.has_canonical_url {
        if has_canonical {
            writeln!(out, "    assert!(result.metadata.canonical_url.is_some());")?;
        } else {
            writeln!(out, "    assert!(result.metadata.canonical_url.is_none());")?;
        }
    }
    Ok(())
}

fn generate_link_assertions(out: &mut String, links: &LinkAssertions) -> Result<()> {
    if let Some(min) = links.min_count {
        writeln!(out, "    assert!(result.links.len() >= {min});")?;
    }
    if let Some(max) = links.max_count {
        writeln!(out, "    assert!(result.links.len() <= {max});")?;
    }
    for link_type in &links.has_type {
        writeln!(
            out,
            "    assert!(result.links.iter().any(|l| l.link_type == \"{}\"));",
            escape_rust_string(link_type)
        )?;
    }
    Ok(())
}

