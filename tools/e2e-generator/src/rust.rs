/// Generate Rust E2E test files from fixtures, grouped by category.
pub fn generate(fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
    let rust_dir = output_root.join("rust");
    let tests_dir = rust_dir.join("tests");
    std::fs::create_dir_all(&tests_dir)
        .with_context(|| format!("creating directory {tests_dir}"))?;

    // Group fixtures by category
    let grouped: Vec<(&str, Vec<&Fixture>)> = fixtures
        .iter()
        .into_group_map_by(|f| f.category())
        .into_iter()
        .sorted_by_key(|(cat, _)| *cat)
        .collect();

    let mut mod_entries = Vec::new();

    for (category, category_fixtures) in &grouped {
        let module_name = sanitize_identifier(category);
        mod_entries.push(module_name.clone());

        let content = generate_category_file(category, category_fixtures)?;
        let file_path = tests_dir.join(format!("{module_name}_test.rs"));
        std::fs::write(&file_path, &content).with_context(|| format!("writing {file_path}"))?;
    }

    // Generate Cargo.toml for the E2E test crate
    let cargo_content = generate_cargo_toml();
    std::fs::write(rust_dir.join("Cargo.toml"), cargo_content)
        .with_context(|| "writing e2e/rust/Cargo.toml")?;

    // Generate helpers module
    let helpers_content = generate_helpers();
    let src_dir = rust_dir.join("src");
    std::fs::create_dir_all(&src_dir).with_context(|| format!("creating directory {src_dir}"))?;
    std::fs::write(src_dir.join("lib.rs"), "pub mod helpers;\n")
        .with_context(|| "writing e2e/rust/src/lib.rs")?;
    std::fs::write(src_dir.join("helpers.rs"), helpers_content)
        .with_context(|| "writing e2e/rust/src/helpers.rs")?;

    let count = fixtures.len();
    let categories = grouped.len();
    println!("Generated {count} tests across {categories} categories in {rust_dir}");

    Ok(())
}

